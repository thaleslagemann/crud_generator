class Clientes:

    def __init__(self, c, codigo, nome):
        self.c = c
        self.tableName = 'clientes'
        self.codigo = codigo
        self.nome = nome

    def getCodigo(codigo):
        return codigo

    def getNome(nome):
        return nome

    def setCodigo(self, codigo):
        self.codigo = codigo

    def setNome(self, nome):
        self.nome = nome

